/**
 * 간이세액표에서 세금 조회
 * @param {number} gross - 과세대상 총지급액
 * @param {number} dependents - 부양가족수
 * @returns {number} 소득세
 */
export function lookupIncomeTax(gross, dependents = 0) {
  // 급여를 100만원 단위로 올림하여 검색
  const bracket = Math.ceil(gross / 1000000) * 1000000;

  // 해당 급여 구간이 없으면 가장 높은 구간 사용
  const taxBracket = Math.min(bracket, Math.max(...Object.keys(SIMPLE_TAX_TABLE).map(Number)));
  const taxTable = SIMPLE_TAX_TABLE[taxBracket];

  if (!taxTable) return 0;

  // 부양가족수 제한 (최대 5명)
  const depCount = Math.min(dependents, 5);
  return taxTable[depCount] || 0;
}

/**
 * 간이세액표 범위 내에서 정확한 선형보간 세금 계산
 * @param {number} gross - 과세대상 총지급액
 * @param {number} dependents - 부양가족수
 * @returns {number} 소득세 (선형보간)
 */
export function calculateIncomeTaxInterpolated(gross, dependents = 0) {
  const depCount = Math.min(dependents, 5);
  const brackets = Object.keys(SIMPLE_TAX_TABLE)
    .map(Number)
    .sort((a, b) => a - b);

  // 최저 구간보다 낮으면 0
  if (gross < brackets[0]) return 0;

  // 최고 구간보다 높으면 최고 구간 세금 사용
  if (gross >= brackets[brackets.length - 1]) {
    return SIMPLE_TAX_TABLE[brackets[brackets.length - 1]][depCount] || 0;
  }

  // 구간 찾기
  let lower = brackets[0];
  let upper = brackets[1];

  for (let i = 0; i < brackets.length - 1; i++) {
    if (gross >= brackets[i] && gross < brackets[i + 1]) {
      lower = brackets[i];
      upper = brackets[i + 1];
      break;
    }
  }

  const lowerTax = SIMPLE_TAX_TABLE[lower][depCount] || 0;
  const upperTax = SIMPLE_TAX_TABLE[upper][depCount] || 0;

  // 선형 보간
  const ratio = (gross - lower) / (upper - lower);
  return Math.round(lowerTax + (upperTax - lowerTax) * ratio);
}
