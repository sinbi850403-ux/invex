/**
 * 엑셀 템플릿 파일 생성 & 다운로드
 * @param {string} templateKey - 템플릿 키 (general, retail, food, manufacturing, warehouse)
 */
export function downloadTemplate(templateKey) {
  const tpl = TEMPLATES[templateKey];
  if (!tpl) return;

  // 워크북 생성
  const wb = XLSX.utils.book_new();

  // 데이터 시트 (실제 입력용)
  const sheetData = [tpl.headers, ...tpl.sampleRows];
  const ws = XLSX.utils.aoa_to_sheet(sheetData);

  // 컬럼 너비 설정 (보기 편하게)
  ws['!cols'] = [
    { wch: 22 },  // 품목명
    { wch: 12 },  // 품목코드
    { wch: 14 },  // 분류
    { wch: 10 },  // 수량
    { wch: 6 },   // 단위
    { wch: 12 },  // 단가(매입)
    { wch: 12 },  // 판매단가
    { wch: 14 },  // 공급가액
    { wch: 12 },  // 부가세
    { wch: 14 },  // 합계금액
    { wch: 16 },  // 창고/위치
    { wch: 20 },  // 비고
  ];

  XLSX.utils.book_append_sheet(wb, ws, '재고 데이터');

  // 안내 시트 (사용법 설명)
  const guideData = [
    ['📌 INVEX 엑셀 양식 사용 안내'],
    [''],
    ['이 엑셀 파일을 INVEX에 업로드하면 자동으로 재고에 등록됩니다.'],
    [''],
    ['■ 필수 컬럼'],
    ['  - 품목명: 상품/자재의 이름 (필수)'],
    ['  - 수량: 현재 재고 수량'],
    [''],
    ['■ 선택 컬럼 (있으면 자동 인식)'],
    ['  - 품목코드: 사내 관리 코드 (SKU)'],
    ['  - 분류: 카테고리 (예: 사무용품, 전자기기)'],
    ['  - 단위: EA, KG, BOX, M 등'],
    ['  - 단가(매입): 1개당 매입 가격'],
    ['  - 판매단가: 1개당 판매 가격 (소가) ← 이익률 계산에 활용'],
    ['  - 공급가액: 수량 × 단가'],
    ['  - 부가세: 공급가액의 10%'],
    ['  - 합계금액: 공급가액 + 부가세 (부가세 포함 가격)'],
    ['  - 창고/위치: 보관 위치'],
    ['  - 비고: 메모, 특이사항'],
    [''],
    ['■ 주의사항'],
    ['  - 1행은 반드시 컬럼 헤더여야 합니다 (품목명, 수량 등)'],
    ['  - 2행부터 실제 데이터를 입력하세요'],
    ['  - 빈 행은 자동으로 건너뜁니다'],
    ['  - 헤더 이름이 다르면 [데이터 확인] 메뉴에서 수동 매핑 가능'],
    [''],
    ['■ 지원 파일'],
    ['  - .xlsx (Excel 2007 이상)', '', '', '', '', '', '', '', ''],
    ['  - .xls (Excel 97-2003)', '', '', '', '', '', '', '', ''],
    ['  - .csv (쉼표 구분)', '', '', '', '', '', '', '', ''],
    [''],
    ['📎 자세한 사용법: https://erp-lite-eight.vercel.app/landing.html'],
  ];
  const guideWs = XLSX.utils.aoa_to_sheet(guideData);
  guideWs['!cols'] = [{ wch: 60 }];
  XLSX.utils.book_append_sheet(wb, guideWs, '사용 안내');

  // 파일 다운로드
  const fileName = `INVEX_${tpl.name.replace(/[^\w가-힣]/g, '')}_양식.xlsx`;
  XLSX.writeFile(wb, fileName);
}

/**
 * 전체 템플릿 목록 반환
 */
export function getTemplateList() {
  return Object.entries(TEMPLATES).map(([key, tpl]) => ({
    key,
    name: tpl.name,
    desc: tpl.desc,
  }));
}
